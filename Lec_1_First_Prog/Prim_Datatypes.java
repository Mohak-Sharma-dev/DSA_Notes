public class Prim_Datatypes {
    public static void main(String[] args) {
        //DATATYPES
        int age = 20;//Age is the identifier, 20 is the literal.
        //4 bytes, or 32 bits
        char a = 'm';
        //2 bytes, or 16 bits
        float b = 20.5f;
        //4 bytes, or 32 bits
        //f is used to specify that it is a float, as default is double.
        double c = 20.5;
        //8 bytes, or 64 bits
        boolean d = true;
        //1 bit, or 1 bit
        long e = 1000000000000000000L;
        //8 bytes, or 64 bits
        //L is used to specify that it is a long, as default is int.
    }
}
