


public class Loops {
    public static void main(String[] args) {

        int count1 = 0;
        int count2 = 1;
        int count3 = 4;

        if(count1 == 0)
        {
            System.out.println("Hello World");
        } 
        
        while(count2 < 5)//If you DON'T know how many times loop will run
        {
            System.out.println("Hello!");
            count2++;
        }

        for(int i = 0 ; i < 5 ; i++)//If you know HOW many times loop will run.
        {
            System.out.println("Hello!!");

        }

        
    }
}
